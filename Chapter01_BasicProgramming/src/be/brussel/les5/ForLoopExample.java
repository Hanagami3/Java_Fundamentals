package be.brussel.les5;

public class ForLoopExample {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++)
            System.out.println(("The index is: " + i));

        System.out.println();

        for (int i = 1; i <= 5; i++)
            System.out.println("Hello World");

        System.out.println();

        for(int i = 10; i >=0 ; i--)
            System.out.println("The counter is: " + i);
    }
}
